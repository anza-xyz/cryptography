#[test]
fn root_extended_signing_key_matches_bip32_vector() {
    let extended = ExtendedSigningKey::from_seed(&SEED);

    assert_eq!(
        extended.signing_key().as_secret_key_bytes(),
        &ROOT_SECRET_KEY
    );
    assert_eq!(extended.chain_code(), &ROOT_CHAIN_CODE);
    assert_eq!(
        <[u8; 32]>::from(extended.signing_key().verification_key()),
        ROOT_PUBLIC_KEY
    );
}

#[test]
fn derived_extended_signing_key_matches_bip32_vector() {
    let extended = ExtendedSigningKey::from_seed(&SEED)
        .derive_path(DERIVED_PATH)
        .expect("hardened path should derive");

    assert_eq!(
        extended.signing_key().as_secret_key_bytes(),
        &DERIVED_SECRET_KEY
    );
    assert_eq!(extended.chain_code(), &DERIVED_CHAIN_CODE);
    assert_eq!(
        <[u8; 32]>::from(extended.signing_key().verification_key()),
        DERIVED_PUBLIC_KEY
    );
}

#[test]
fn signing_key_convenience_constructor_matches_bip32_vector() {
    let signing_key = SigningKey::from_bip32_seed_and_hardened_path(&SEED, DERIVED_PATH)
        .expect("hardened path should derive");

    assert_eq!(signing_key.as_secret_key_bytes(), &DERIVED_SECRET_KEY);
}

#[test]
fn derivation_rejects_normal_child_indexes() {
    let err = ExtendedSigningKey::from_seed(&SEED)
        .derive_child(0)
        .expect_err("normal child index should be rejected");

    assert_eq!(err, Bip32DerivationError::ExpectedHardenedChildIndex(0));
    assert!(
        ExtendedSigningKey::from_seed(&SEED)
            .derive_child(BIP32_HARDENED_INDEX_FLAG)
            .is_ok()
    );
}
